CREATE TABLE `ST_BATCH_JOB_EXECUTION` (
  `JOB_EXECUTION_ID` bigint(20) unsigned NOT NULL AUTO_INCREMENT COMMENT '배치로그 PK',
  `BATCH_NO` bigint(20) unsigned NOT NULL COMMENT '실행배치번호',
  `START_TIME` timestamp DEFAULT NULL,
  `END_TIME` timestamp DEFAULT NULL,
  `STATUS` varchar(10) CHARACTER SET utf8 COLLATE utf8_general_ci DEFAULT NULL COMMENT '실행상태',
  `ERROR_MSG` mediumtext CHARACTER SET utf8 COLLATE utf8_general_ci COMMENT '에러내용',
  PRIMARY KEY (`JOB_EXECUTION_ID`) USING BTREE
) ENGINE=InnoDB DEFAULT CHARSET=utf8 COMMENT='배치로그'